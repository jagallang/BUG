  int _getTestPeriod(MissionCard mission) {
    if (mission.isProviderApp && mission.originalAppData != null) {
      final metadata = mission.originalAppData!['metadata'] as Map<String, dynamic>?;
      return _toInt(metadata?['testPeriod'], 14);
    }
    return 14; // Í∏∞Î≥∏Í∞í
  }

